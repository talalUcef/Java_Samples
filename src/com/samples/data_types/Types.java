public class Types {

    // Java is a strongly typed language

    // Primitives types in java are :
    //  # Integers : byle (8 bit), short (16 bis), int (32 bit) and long(64 bit)
    //  # Floting point numbers : double(64 bit) and float (32 bit)
    //  # Characters : char
    //  # Boolean : boulean


    public static void main( String[] args) {

        // byte type (8 bits)
        // Holds integers between -2^7 and (2^7 - 1)
        byte b1 = 23;
        byte b2 = -18;
        System.out.println("b1 = " + b1);
        System.out.println("b2 = " + b2);

        // short type (16 bits)
        // Holds integers between -2^15 and (2^15 - 1)
        short s1 = 244;
        short s2 = -334;
        System.out.println("s1 = " + s1);
        System.out.println("s2 = " + s2);

        // int type (32 bits)
        // Holds integers between -2^31 and (2^31 - 1)
        int i1 = 3555;
        int i2 = -434534;
        System.out.println("i1 = " + i1);
        System.out.println("i2 = " + i2);




    }
}
